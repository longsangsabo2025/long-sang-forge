-- 1. Agent Registry (master list of all AI agents)
CREATE TABLE IF NOT EXISTS agent_registry (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  
  -- Identity
  name TEXT NOT NULL,
  codename TEXT UNIQUE NOT NULL,
  department TEXT NOT NULL,
  avatar_emoji TEXT DEFAULT '🤖',
  description TEXT,
  
  -- Classification
  type TEXT NOT NULL CHECK (type IN ('pipeline', 'autonomous', 'on-demand', 'scheduled', 'reactive')),
  source_project TEXT,          -- "youtube-agent-crew", "long-sang-forge", "agentkits-marketing", etc.
  source_file TEXT,             -- relative path to agent code
  
  -- Capability
  model TEXT DEFAULT 'gemini-2.0-flash',
  tools JSONB DEFAULT '[]',
  skills JSONB DEFAULT '[]',
  
  -- Employment
  status TEXT DEFAULT 'idle' CHECK (status IN ('active', 'idle', 'busy', 'error', 'disabled', 'training')),
  hired_at TIMESTAMPTZ DEFAULT now(),
  last_active_at TIMESTAMPTZ,
  
  -- Performance KPI
  total_executions INTEGER DEFAULT 0,
  success_count INTEGER DEFAULT 0,
  fail_count INTEGER DEFAULT 0,
  success_rate NUMERIC(5,2) DEFAULT 100.00,
  avg_execution_time_ms INTEGER DEFAULT 0,
  total_cost_usd NUMERIC(10,4) DEFAULT 0,
  total_revenue_usd NUMERIC(10,4) DEFAULT 0,
  quality_score NUMERIC(3,2) DEFAULT 5.00,
  
  -- Config
  schedule TEXT,                -- cron expression
  config JSONB DEFAULT '{}',
  cost_per_run NUMERIC(10,6) DEFAULT 0,
  
  -- Organization
  reports_to UUID REFERENCES agent_registry(id),
  company_id UUID,             -- SABOHUB company link
  
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now()
);

-- 2. Agent Execution Log
CREATE TABLE IF NOT EXISTS agent_execution_log (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  agent_id UUID REFERENCES agent_registry(id) NOT NULL,
  
  -- Execution
  trigger_type TEXT NOT NULL DEFAULT 'manual',  -- "cron", "manual", "pipeline", "webhook", "realtime"
  input JSONB,
  output JSONB,
  status TEXT DEFAULT 'running' CHECK (status IN ('running', 'success', 'failed', 'timeout', 'cancelled')),
  error_message TEXT,
  
  -- Metrics
  started_at TIMESTAMPTZ DEFAULT now(),
  completed_at TIMESTAMPTZ,
  duration_ms INTEGER,
  cost_usd NUMERIC(10,6) DEFAULT 0,
  tokens_used INTEGER DEFAULT 0,
  
  -- Context
  pipeline_id TEXT,
  stage_number INTEGER,
  parent_execution_id UUID REFERENCES agent_execution_log(id),
  
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 3. Agent Daily Report (generated by pg_cron at 11 PM)
CREATE TABLE IF NOT EXISTS agent_daily_report (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  agent_id UUID REFERENCES agent_registry(id) NOT NULL,
  report_date DATE DEFAULT CURRENT_DATE,
  
  executions_count INTEGER DEFAULT 0,
  success_count INTEGER DEFAULT 0,
  fail_count INTEGER DEFAULT 0,
  total_cost_usd NUMERIC(10,4) DEFAULT 0,
  total_revenue_usd NUMERIC(10,4) DEFAULT 0,
  avg_quality_score NUMERIC(3,2),
  
  highlights JSONB DEFAULT '[]',
  issues JSONB DEFAULT '[]',
  kpi_data JSONB DEFAULT '{}',
  
  UNIQUE(agent_id, report_date),
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 4. Agent Messages (agent-to-agent or CEO-to-agent communication)
CREATE TABLE IF NOT EXISTS agent_messages (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  from_agent_id UUID REFERENCES agent_registry(id),
  to_agent_id UUID REFERENCES agent_registry(id),
  from_user_id UUID,          -- if from CEO
  
  message_type TEXT DEFAULT 'task' CHECK (message_type IN ('task', 'report', 'alert', 'handoff', 'question', 'decision')),
  content TEXT NOT NULL,
  metadata JSONB DEFAULT '{}',
  
  read_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ DEFAULT now()
);

