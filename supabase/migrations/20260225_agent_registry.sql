-- Updated_at trigger
CREATE OR REPLACE FUNCTION update_agent_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER agent_registry_updated_at
  BEFORE UPDATE ON agent_registry
  FOR EACH ROW
  EXECUTE FUNCTION update_agent_updated_at();

-- Function: Update agent KPI after execution
CREATE OR REPLACE FUNCTION update_agent_kpi()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.status IN ('success', 'failed') AND NEW.completed_at IS NOT NULL THEN
    UPDATE agent_registry SET
      total_executions = total_executions + 1,
      success_count = success_count + CASE WHEN NEW.status = 'success' THEN 1 ELSE 0 END,
      fail_count = fail_count + CASE WHEN NEW.status = 'failed' THEN 1 ELSE 0 END,
      success_rate = ROUND(
        (success_count + CASE WHEN NEW.status = 'success' THEN 1 ELSE 0 END)::NUMERIC /
        NULLIF(total_executions + 1, 0) * 100, 2
      ),
      avg_execution_time_ms = CASE
        WHEN total_executions = 0 THEN COALESCE(NEW.duration_ms, 0)
        ELSE ROUND((avg_execution_time_ms * total_executions + COALESCE(NEW.duration_ms, 0))::NUMERIC / (total_executions + 1))
      END,
      total_cost_usd = total_cost_usd + COALESCE(NEW.cost_usd, 0),
      last_active_at = NEW.completed_at,
      status = 'idle'
    WHERE id = NEW.agent_id;
  ELSIF NEW.status = 'running' THEN
    UPDATE agent_registry SET status = 'busy', last_active_at = now()
    WHERE id = NEW.agent_id;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER agent_execution_kpi_update
  AFTER INSERT OR UPDATE ON agent_execution_log
  FOR EACH ROW
  EXECUTE FUNCTION update_agent_kpi();

-- Function: Generate daily report (called by pg_cron at 23:00)
CREATE OR REPLACE FUNCTION generate_agent_daily_reports()
RETURNS void AS $$
BEGIN
  INSERT INTO agent_daily_report (agent_id, report_date, executions_count, success_count, fail_count, total_cost_usd)
  SELECT
    agent_id,
    CURRENT_DATE,
    COUNT(*),
    COUNT(*) FILTER (WHERE status = 'success'),
    COUNT(*) FILTER (WHERE status = 'failed'),
    COALESCE(SUM(cost_usd), 0)
  FROM agent_execution_log
  WHERE created_at >= CURRENT_DATE
    AND created_at < CURRENT_DATE + INTERVAL '1 day'
    AND status IN ('success', 'failed')
  GROUP BY agent_id
  ON CONFLICT (agent_id, report_date)
  DO UPDATE SET
    executions_count = EXCLUDED.executions_count,
    success_count = EXCLUDED.success_count,
    fail_count = EXCLUDED.fail_count,
    total_cost_usd = EXCLUDED.total_cost_usd;
END;
$$ LANGUAGE plpgsql;

