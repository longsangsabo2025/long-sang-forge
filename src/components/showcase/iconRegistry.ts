/**
 * Get icon component by name
 * Returns Sparkles as default if icon not found
 */
export const getIconByName = (iconName: string): IconComponent => {
  return iconRegistry[iconName] || Sparkles;
};

/**
 * Check if icon exists in registry
 */
export const hasIcon = (iconName: string): boolean => {
  return iconName in iconRegistry;
};

/**
 * Get all available icon names
 */
export const getAvailableIcons = (): string[] => {
  return Object.keys(iconRegistry);
};
